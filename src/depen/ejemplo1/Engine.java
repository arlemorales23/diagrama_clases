package depen.ejemplo1;

public class Engine {

    public void start() {
        System.out.println("The engine has started.");
    }

    public void accelerate() {
        System.out.println("The engine is accelerating.");
    }

    public void brake() {
        System.out.println("The engine is braking.");
    }

    public void turnOff() {
        System.out.println("The engine has turned off.");
    }
}
